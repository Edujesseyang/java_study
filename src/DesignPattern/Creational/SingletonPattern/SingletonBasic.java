public class SingletonBasic {
    /*
    * 单例模式保证一个类只有一个实例（instance），并提供一个**全局访问点（global access point）**来访问它。
    *
    * 适用场景
        系统中某个类的对象只需要一个，比如：

        数据库连接池（Database Connection Pool）

        日志管理器（Logger）

        配置中心（Configuration Manager）

        驱动程序管理（Driver Manager）

     单例的三个关键要素
        私有构造函数：防止外部 new 出多个实例

        静态成员变量：保存唯一实例

        静态公共方法：提供访问入口
        *
        *
        *
    * */
}
